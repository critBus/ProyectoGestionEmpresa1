    private void B_AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_AceptarActionPerformed
        try {
            String nombre=T_Nombre.getText();
            String apellidos=T_Apellidos.getText();
            String cargo=T_Cargo.getText();
            String correo=T_Correo.getText();
            String direccion=T_Direccion.getText();
            String telefono=T_Telefono.getText();
            if(Validaciones.validar_es_nuevo_Encargado_correcto(this, localActual, nombre, apellidos, cargo, correo, direccion, telefono)){
                Encargado nuevoEncargado=Referencias.BD.agregarEncargado(localActual, nombre, apellidos, cargo, correo, direccion, telefono);
                Referencias.ventana_principal.actualizar_Encargado(nuevoEncargado); 
                setVisible(false);
                DialogosDeRespuesta.mostrarDlgExito(this, "Encargado Agregado con Ã©xito  ");
            }
        } catch (Exception ex) {
            DialogosDeRespuesta.mostrarDlg_ErrorEnLaBD(this, ex);
        }
        
    }//GEN-LAST:event_B_AceptarActionPerformed

